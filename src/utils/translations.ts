export function translateDoctorName(name: string, lang: Language): string {
  const normalized = name.toLowerCase().trim();
  const transMap = DOCTOR_NAMES_TRANSLATIONS[normalized];
  if (transMap && transMap[lang]) {
    return transMap[lang];
  }
  return name;
}

export function translateUI(key: string, lang: Language): string {
  const dict = UI_DICTIONARY[key];
  if (!dict) return key;
  return dict[lang] || dict['en'] || key;
}


