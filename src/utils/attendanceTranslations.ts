export function translateAttendanceUI(key: string, lang: Language): string {
  const dict = ATTENDANCE_TRANSLATIONS[key];
  if (!dict) return key;
  return dict[lang] || dict['en'] || key;
}

// Helper to translate status labels ('Active' / 'Off-Duty')
export function translateAttendanceStatus(status: 'Active' | 'Off-Duty', lang: Language): string {
  if (status === 'Active') {
    return ATTENDANCE_TRANSLATIONS.active[lang] || ATTENDANCE_TRANSLATIONS.active.en;
  }
  return ATTENDANCE_TRANSLATIONS.offDuty[lang] || ATTENDANCE_TRANSLATIONS.offDuty.en;
}

// Helper to translate specializations
export function translateSpecialization(spec: string, lang: Language): string {
  if (spec.toLowerCase().includes('physician')) {
    return ATTENDANCE_TRANSLATIONS.generalPhysician[lang] || ATTENDANCE_TRANSLATIONS.generalPhysician.en;
  }
  if (spec.toLowerCase().includes('pediatrician')) {
    return ATTENDANCE_TRANSLATIONS.pediatrician[lang] || ATTENDANCE_TRANSLATIONS.pediatrician.en;
  }
  if (spec.toLowerCase().includes('snakebite')) {
    return ATTENDANCE_TRANSLATIONS.snakebiteSpecialist[lang] || ATTENDANCE_TRANSLATIONS.snakebiteSpecialist.en;
  }
  if (spec.toLowerCase().includes('surgeon')) {
    return ATTENDANCE_TRANSLATIONS.emergencySurgeon[lang] || ATTENDANCE_TRANSLATIONS.emergencySurgeon.en;
  }
  return spec;
}

// Helper to translate shift
export function translateShift(shift: 'Day' | 'Night', lang: Language): string {
  if (shift === 'Day') {
    return ATTENDANCE_TRANSLATIONS.dayShift[lang] || ATTENDANCE_TRANSLATIONS.dayShift.en;
  }
  return ATTENDANCE_TRANSLATIONS.nightShift[lang] || ATTENDANCE_TRANSLATIONS.nightShift.en;
}

